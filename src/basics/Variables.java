package basics;

public class Variables {
    public static void main(String[] args) {
        //Declaration
        int speed;
        //Assignation
        speed = 10;
        System.out.println(speed);

        //Initialization
        int salary = 1000;
        System.out.println(salary);

        //String
        String employeeName = "David C";
        System.out.println(employeeName);
    }
}
